// Подсчёт правильных ответов
export function scoreFor(answers) {
  return QUESTIONS.reduce((acc, q, i) => acc + (answers[i] === q.correct ? 1 : 0), 0);
}

// Позитивная интерпретация результата (от силы)
export function strengthReport(answers) {
  const known = [];
  const toRefresh = [];
  QUESTIONS.forEach((q, i) => {
    const item = { theme: q.theme, lesson: q.lesson, explanation: q.explanation };
    if (answers[i] === q.correct) known.push(item);
    else toRefresh.push(item);
  });
  return { total: QUESTIONS.length, score: known.length, known, toRefresh };
}
